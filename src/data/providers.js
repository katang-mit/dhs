export const getProviderById = (id) => {
  return providers.find(provider => provider.id === id)
}

export const searchProviders = (filters) => {
  let filtered = [...providers]

  if (filters.type && filters.type !== 'all') {
    filtered = filtered.filter(p => p.type === filters.type)
  }

  if (filters.specialty && filters.specialty !== 'all') {
    filtered = filtered.filter(p => p.specialty === filters.specialty)
  }

  if (filters.language && filters.language !== 'all') {
    filtered = filtered.filter(p => p.languages.includes(filters.language))
  }

  if (filters.acceptingNewPatients) {
    filtered = filtered.filter(p => p.acceptingNewPatients)
  }

  if (filters.gender && filters.gender !== 'all') {
    filtered = filtered.filter(p => p.gender === filters.gender)
  }

  if (filters.searchTerm) {
    const term = filters.searchTerm.toLowerCase()
    filtered = filtered.filter(p =>
      p.name.toLowerCase().includes(term) ||
      p.specialty.toLowerCase().includes(term) ||
      p.organization.toLowerCase().includes(term)
    )
  }

  return filtered
}

export const getUniqueSpecialties = () => {
  const specialties = [...new Set(providers.map(p => p.specialty))]
  return specialties.sort()
}

export const getUniqueLanguages = () => {
  const languages = new Set()
  providers.forEach(p => {
    p.languages.forEach(lang => languages.add(lang))
  })
  return Array.from(languages).sort()
}
